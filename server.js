const express = require("express");
const mongoose = require("mongoose");
const cors = require("cors");
require("dotenv").config();

const studentRoutes = require("./routes/studentRoutes");

const app = express();

// CORS - allow all origins for testing (change to your frontend URL for production)
app.use(cors({
  origin: "*",
  methods: ["GET", "POST", "PUT", "DELETE"],
  allowedHeaders: ["Content-Type", "Authorization"]
}));

app.use(express.json());

// Routes
app.use("/api/students", studentRoutes);

// MongoDB connection
mongoose.connect(process.env.MONGO_URI, {
  useNewUrlParser: true,
  useUnifiedTopology: true
})
.then(() => console.log("âœ… MongoDB connected successfully"))
.catch(err => {
  console.error("âŒ MongoDB connection error:", err);
  // exit process if DB not connected (optional)
  // process.exit(1);
});

// Simple root route to let Render wake endpoint fast
app.get("/", (req, res) => {
  res.json({ message: "Student Record Management API is running" });
});

const PORT = process.env.PORT || 5000;
app.listen(PORT, () => {
  console.log(`ðŸš€ Server running on port ${PORT}`);
});
